/**
 * Icona generica utilizzata
 * soltanto se non troviamo
 * una combinazione type/value valida.
 */
const FALLBACK_PATHS: string[] = [
  'M30 40 C38 25 48 19 60 19 C72 19 82 25 90 40 C82 55 72 61 60 61 C48 61 38 55 30 40'
];

/**
 * Componente visuale riutilizzabile.
 */
@Component({
  selector: 'app-profile-visual-icon',
  standalone: true,
  imports: [],
  templateUrl: './profile-visual-icon.html',
  styleUrl: './profile-visual-icon.css'
})
export class ProfileVisualIconComponent {

  /**
   * Categoria grafica.
   */
  @Input({
    required: true
  })
  type!: ProfileVisualIconType;

  /**
   * Valore Enum ricevuto
   * dal FaceProfile.
   */
  @Input()
  value:
    string |
    null |
    undefined;

  /**
   * Recupera i path SVG
   * corrispondenti.
   */
  protected get paths(): string[] {

    if (
      !this.type ||
      !this.value
    ) {

      return FALLBACK_PATHS;
    }

    return (
      ICON_PATHS[this.type]?.[this.value] ??
      FALLBACK_PATHS
    );
  }
}